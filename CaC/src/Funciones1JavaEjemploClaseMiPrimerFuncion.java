/*
 * Se plantea la zona donde debe alojarse un método static cuando aún no se vé orientación a objetos.
 */
public class Funciones1JavaEjemploClaseMiPrimerFuncion {
    //Zona de las funciones, procedimientos y métodos
    static void saludo(String nombre){
        System.out.println("Hola "+nombre);
    }
    public static void main(String[] args) {
        saludo("Codo a Codo");
    }
}
